function avgThreeNumbers(num1, num2 ,num3) {
    return (num1 + num2 + num3) / 3 ;
}

const number1 = 10 
const number2 = 15
const number3 = 35

const average = avgThreeNumbers(number1,number2,number3);

console.log("The average of " + number1 + ", " + number2 + ", and " + number3 + " is: " + average);
